#!/usr/bin/env node
/**
 * build-codex.mjs — one-way generator: Claude Code plugin skills → Codex Agent Skills (★).
 *
 * The Claude plugins under `plugins/<p>/skills/<s>/` are the SINGLE SOURCE OF TRUTH.
 * This script emits a Codex-discoverable mirror under `.agents/skills/<p>-<s>/` plus a
 * repo-root `AGENTS.md` synthesised from plugin `CLAUDE.md` files. Re-run after any
 * skill edit; never hand-edit the generated output (it is overwritten).
 *
 * This file owns ONLY filesystem discovery/copy/walk. Every Codex-specific text transform
 * (frontmatter lowering, worker-dispatch prose, project-guidance/namespace rewrites, the
 * unsupported-token scan) lives in scripts/lib/codex-compat.mjs — the compiler this script calls
 * once per file. See blueprints/plans/2026-07-13-codex-compatibility.md Phase 1.
 *
 * Reads: node:fs · node:path (plugins/ tree · plugins/<p>/CLAUDE.md)
 */
import { readFileSync, writeFileSync, readdirSync, mkdirSync, rmSync, existsSync, statSync, cpSync } from "node:fs";
import { join, dirname, basename } from "node:path";
import { fileURLToPath } from "node:url";
import {
  codexDescription,
  findInstalledSkillCopies,
  loadCodexProjection,
  selectProfile,
} from "./lib/codex-projection.mjs";
import {
  createProjectGuidanceLowerer,
  detectUnsupported,
  injectWorkerDispatchContract,
  lowerFrontmatter,
  lowerWorkerProse,
} from "./lib/codex-compat.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const PLUGINS_DIR = join(ROOT, "plugins");
const OUT_DIR = join(ROOT, ".agents", "skills");
const AGENTS_MD = join(ROOT, "AGENTS.md");
const PROJECTION = loadCodexProjection(ROOT);

const PLUGINS = readdirSync(PLUGINS_DIR)
  .filter((plugin) => {
    const pluginDir = join(PLUGINS_DIR, plugin);
    return (
      statSync(pluginDir).isDirectory() &&
      existsSync(join(pluginDir, "CLAUDE.md")) &&
      existsSync(join(pluginDir, "skills"))
    );
  })
  .sort();
const SKILLS = [
  ...new Set(
    PLUGINS.flatMap((plugin) =>
      readdirSync(join(PLUGINS_DIR, plugin, "skills")).filter((skill) =>
        statSync(join(PLUGINS_DIR, plugin, "skills", skill)).isDirectory(),
      ),
    ),
  ),
].sort();
const GUIDANCE = createProjectGuidanceLowerer({ plugins: PLUGINS, skills: SKILLS });
const CODEX_DESCRIPTION_HARD_LIMIT = 1024;

/** Bump the frontmatter `name:` to the flattened Codex name (first occurrence only). */
function renameFrontmatter(text, bare, flat) {
  return text.replace(new RegExp(`^name:\\s*${bare}\\s*$`, "m"), `name: ${flat}`);
}

/**
 * Codex validates metadata more strictly than Claude plugins do. The projection sidecar
 * supplies the normal budget; this hard limit remains a final format-safety backstop.
 */
function normalizeCodexFrontmatter(text, flatName) {
  return text.replace(/^---\n([\s\S]*?)\n---/, (_match, frontmatter) => {
    const normalized = frontmatter.replace(/^description:\s*(.*)$/m, (_line, rawDescription) => {
      const sourceDescription = parseFrontmatterString(rawDescription.trim()).replace(/\s+/g, " ").trim();
      let description = codexDescription(PROJECTION, flatName, sourceDescription);
      if (description.length > CODEX_DESCRIPTION_HARD_LIMIT) {
        description = `${description.slice(0, CODEX_DESCRIPTION_HARD_LIMIT - 3).trimEnd()}...`;
      }
      return `description: ${JSON.stringify(description)}`;
    });
    return `---\n${normalized}\n---`;
  });
}

function parseFrontmatterString(raw) {
  try {
    const parsed = JSON.parse(raw);
    return typeof parsed === "string" ? parsed : raw;
  } catch {
    return raw;
  }
}

const GEN_BANNER_SKILL = "<!-- GENERATED by scripts/build-codex.mjs from plugins/{plugin}/skills/{skill}/ — do not hand-edit; re-run the generator. -->\n";

/**
 * The Codex compiler pipeline for one SKILL.md: rename → project-guidance lowering (namespace
 * flatten, reference reroot, CLAUDE.md→AGENTS.md, /init softening) → frontmatter lowering
 * (strip model:sonnet/disable-model-invocation, inject the Codex-side instruction) → worker-prose
 * lowering (Agent/subagent_type/sub-agent constructs + remaining prose mentions of the two
 * stripped frontmatter fields) → worker-dispatch contract injection (Phase 2, dispatch-heavy
 * skills only) → Codex description-budget normalization → generated banner.
 */
function compileSkillMarkdown(rawText, { plugin, skill, flat }) {
  let text = renameFrontmatter(rawText, skill, flat);
  text = GUIDANCE.lowerSkillGuidance(text);
  const { text: withoutFrontmatterFields } = lowerFrontmatter(text);
  text = lowerWorkerProse(withoutFrontmatterFields);
  text = injectWorkerDispatchContract(text, flat);
  text = normalizeCodexFrontmatter(text, flat);
  const banner = GEN_BANNER_SKILL.replace("{plugin}", plugin).replace("{skill}", skill);
  text = text.replace(/^(---\n[\s\S]*?\n---\n)/, `$1${banner}`);
  return text;
}

/** Reference/asset text files (*.md/*.html/*.txt) travel with a skill dir: same project-guidance
 * + worker-prose lowering, no frontmatter (they don't have any) and no banner (the skill's own
 * SKILL.md banner already marks the directory as generated). */
function compileReferenceText(rawText) {
  return lowerWorkerProse(GUIDANCE.lowerSkillGuidance(rawText));
}

function build() {
  if (existsSync(OUT_DIR)) rmSync(OUT_DIR, { recursive: true });
  mkdirSync(OUT_DIR, { recursive: true });

  const emitted = [];
  for (const plugin of PLUGINS) {
    const skillsDir = join(PLUGINS_DIR, plugin, "skills");
    if (!existsSync(skillsDir)) continue;
    for (const skill of readdirSync(skillsDir)) {
      const srcSkillDir = join(skillsDir, skill);
      if (!statSync(srcSkillDir).isDirectory()) continue;
      const srcSkillMd = join(srcSkillDir, "SKILL.md");
      if (!existsSync(srcSkillMd)) continue;

      const flat = `${plugin}-${skill}`;
      const outSkillDir = join(OUT_DIR, flat);
      mkdirSync(outSkillDir, { recursive: true });

      const md = compileSkillMarkdown(readFileSync(srcSkillMd, "utf8"), { plugin, skill, flat });
      writeFileSync(join(outSkillDir, "SKILL.md"), md);

      // Bundled resources (references/, assets/, scripts/): copy verbatim, compiling *.md/*.html/*.txt.
      for (const sub of ["references", "assets", "scripts"]) {
        const srcSub = join(srcSkillDir, sub);
        if (!existsSync(srcSub)) continue;
        const outSub = join(outSkillDir, sub);
        cpSync(srcSub, outSub, { recursive: true });
        for (const f of walk(outSub)) {
          if (/\.(md|html|txt)$/.test(f)) writeFileSync(f, compileReferenceText(readFileSync(f, "utf8")));
        }
      }
      emitted.push(flat);
    }
  }

  buildAgentsMd();
  reportUnsupported(emitted);
  console.log(`✓ ${emitted.length} Codex skills → .agents/skills/  (${emitted.join(", ")})`);
  console.log(`✓ AGENTS.md (from CLAUDE.md + ${PLUGINS.map((p) => `plugins/${p}/CLAUDE.md`).join(" + ")})`);
}

/** Synthesise repo-root AGENTS.md: the repo-root CLAUDE.md (repo-wide rules) + each plugin's CLAUDE.md.
 * Same project-guidance + worker-prose lowering as skill bodies, so AGENTS.md never leaks
 * `model: sonnet` / `disable-model-invocation` prose describing the authoring convention itself. */
function buildAgentsMd() {
  const preamble = [
    "# AGENTS.md — `skills` marketplace",
    "",
    "<!-- GENERATED by scripts/build-codex.mjs from CLAUDE.md + plugins/*/CLAUDE.md — do not hand-edit; re-run the generator. -->",
    "",
    "> Context for an agent working in **this repo**. The source of truth is the Claude Code plugins under",
    "> `plugins/<plugin>/skills/<skill>/SKILL.md`. Their Codex mirror lives in `.agents/skills/<plugin>-<skill>/`",
    "> (flat, unnamespaced — Codex has no plugin namespace) and is **generated** by `scripts/build-codex.mjs`.",
    "> Edit the plugin skills, then re-run the generator; never hand-edit `.agents/skills/` or this file.",
    "",
    "Each `SKILL.md` is self-contained (it restates the rules it needs). The repo-wide rules for editing this",
    "marketplace follow (mirrored from the repo-root `CLAUDE.md`), then each plugin's own conventions.",
  ].join("\n");

  let rootRules = readFileSync(join(ROOT, "CLAUDE.md"), "utf8");
  rootRules = lowerWorkerProse(
    GUIDANCE.lowerSharedGuidance(rootRules.replace(/^#\s+CLAUDE\.md.*$/m, "# marketplace — repo-wide rules (editing this repo)")),
  );

  const sections = PLUGINS.map((plugin) => {
    let body = readFileSync(join(PLUGINS_DIR, plugin, "CLAUDE.md"), "utf8");
    body = body.replace(/^#\s+.*plugin-level CLAUDE\.md\s*$/m, `# ${plugin} — plugin conventions`);
    return lowerWorkerProse(GUIDANCE.lowerSharedGuidance(body));
  });

  writeFileSync(AGENTS_MD, [preamble, rootRules, ...sections].join("\n\n---\n\n") + "\n");
}

/** Informational only (never fails the build): summarizes which denylisted-token categories
 * still remain in generated output — expected for capabilities later phases own (interactive_choice,
 * browser_verify, session_open_awareness). Uses the same manifest-driven rule list the validator's
 * --compat-audit door reports in full; this is just a quick per-build signal. */
function reportUnsupported(emittedFlats) {
  const counts = new Map();
  const skillMd = emittedFlats.map((flat) => join(OUT_DIR, flat, "SKILL.md"));
  const agentsMd = [AGENTS_MD];
  for (const file of [...skillMd, ...agentsMd]) {
    if (!existsSync(file)) continue;
    const rel = file.replace(`${ROOT}/`, "");
    const findings = detectUnsupported(ROOT, rel, readFileSync(file, "utf8"));
    for (const finding of findings) counts.set(finding.category, (counts.get(finding.category) || 0) + 1);
  }
  if (!counts.size) return;
  const summary = [...counts.entries()].sort().map(([id, n]) => `${id}=${n}`).join(", ");
  console.log(`  (remaining denylisted-token categories, later phases: ${summary})`);
}

function* walk(dir) {
  for (const e of readdirSync(dir, { withFileTypes: true })) {
    const p = join(dir, e.name);
    if (e.isDirectory()) yield* walk(p);
    else yield p;
  }
}

build();

if (process.argv.includes("--sync-global")) {
  syncGlobal();
}

function syncGlobal() {
  const HOME = process.env.HOME || process.env.USERPROFILE;
  if (!HOME) return;
  const globalDir = join(HOME, ".agents", "skills");
  if (!existsSync(globalDir)) {
    console.log(`\n→ Global directory ~/.agents/skills does not exist. Skipping global sync.`);
    return;
  }

  console.log(`\n→ Syncing to global directory: ${globalDir}`);
  const profileName = argValue("--profile") ?? "all";
  const available = PLUGINS.flatMap((plugin) =>
    readdirSync(join(PLUGINS_DIR, plugin, "skills"))
      .filter((skill) => existsSync(join(PLUGINS_DIR, plugin, "skills", skill, "SKILL.md")))
      .map((skill) => `${plugin}-${skill}`),
  ).sort();
  const selected = selectProfile(PROJECTION, profileName, available);
  const selectedSet = new Set(selected);
  const receiptPath = join(globalDir, ".skills-marketplace-codex.json");
  const previous = existsSync(receiptPath)
    ? JSON.parse(readFileSync(receiptPath, "utf8")).skills ?? []
    : [];
  for (const flat of available) {
    if (isGeneratedMarketplaceSkill(join(globalDir, flat, "SKILL.md"))) previous.push(flat);
  }

  for (const flat of new Set(previous)) {
    if (selectedSet.has(flat)) continue;
    const stale = join(globalDir, flat);
    if (existsSync(stale)) rmSync(stale, { recursive: true });
  }

  let syncedCount = 0;

  for (const plugin of PLUGINS) {
    const skillsDir = join(PLUGINS_DIR, plugin, "skills");
    if (!existsSync(skillsDir)) continue;
    for (const skill of readdirSync(skillsDir)) {
      const srcSkillDir = join(skillsDir, skill);
      if (!statSync(srcSkillDir).isDirectory()) continue;

      const flat = `${plugin}-${skill}`;
      if (!selectedSet.has(flat)) continue;

      // Copy the COMPILED Codex output (never raw Claude source) — both prefixed and any
      // unprefixed install target must read from .agents/skills/, which already went through
      // the full lowering pipeline above.
      const compiledSkillDir = join(OUT_DIR, flat);
      const destPrefixedDir = join(globalDir, flat);
      if (existsSync(compiledSkillDir)) {
        if (existsSync(destPrefixedDir)) rmSync(destPrefixedDir, { recursive: true });
        cpSync(compiledSkillDir, destPrefixedDir, { recursive: true });
        syncedCount++;
      }
    }
  }
  writeFileSync(receiptPath, `${JSON.stringify({ profile: profileName, skills: selected }, null, 2)}\n`);
  console.log(`✓ Synced ${syncedCount} compiled skills with Codex profile "${profileName}".`);
  console.log("✓ Unprefixed Claude-source skills were not copied; Codex installs only the compiled names.");

  if (process.argv.includes("--dedupe-global-roots")) {
    const legacyRoot = join(HOME, ".codex", "skills");
    let removed = 0;
    for (const flat of available) {
      const legacyDir = join(legacyRoot, flat);
      if (!isGeneratedMarketplaceSkill(join(legacyDir, "SKILL.md"))) continue;
      rmSync(legacyDir, { recursive: true });
      removed++;
    }
    console.log(`✓ Removed ${removed} generated duplicate marketplace skills from ~/.codex/skills.`);
  }

  const copies = findInstalledSkillCopies(HOME, selected);
  const duplicates = [...copies.entries()].filter(([, paths]) => paths.length > 1);
  if (duplicates.length) {
    console.warn("\n⚠ Duplicate Codex skill copies remain across ~/.agents/skills and ~/.codex/skills:");
    for (const [name, paths] of duplicates) console.warn(`  ${name}: ${paths.join(" · ")}`);
    console.warn("  Re-run with --dedupe-global-roots to remove generated marketplace duplicates.");
  }
}

function argValue(flag) {
  const index = process.argv.indexOf(flag);
  return index === -1 ? null : process.argv[index + 1] ?? null;
}

function isGeneratedMarketplaceSkill(skillMd) {
  if (!existsSync(skillMd)) return false;
  return readFileSync(skillMd, "utf8").includes(
    "GENERATED by scripts/build-codex.mjs from plugins/",
  );
}
